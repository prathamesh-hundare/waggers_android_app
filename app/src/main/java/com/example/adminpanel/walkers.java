package com.example.adminpanel;

public class walkers {
    String name;
    String age;
    String phone;

    public walkers(String name,String age,String phone){
        this.name=name;
        this.age=age;
        this.phone=phone;
    }
    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public String getPhone() {
        return phone;
    }
}
