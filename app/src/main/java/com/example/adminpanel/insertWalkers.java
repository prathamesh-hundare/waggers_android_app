package com.example.adminpanel;

public class insertWalkers {
    String name;
    String age;
    String no;

    public insertWalkers(String name, String age, String no) {
        this.name = name;
        this.age = age;
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public String getAge() {
        return age;
    }

    public String getNo() {
        return no;
    }
}
